package com.fujitsu.fac.dtos;

public class AttendeeDTO {

	private String fNme = "";
	private String lNme = "";
	private String email = "";
	private String compNme = "";
	private String pos = "";
	private String phne = "";
	private String mobile = "";
	private String role = "";
	
	public AttendeeDTO() {
	}

	public String getfNme() {
		return fNme;
	}

	public void setfNme(String fNme) {
		this.fNme = fNme;
	}

	public String getlNme() {
		return lNme;
	}

	public void setlNme(String lNme) {
		this.lNme = lNme;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCompNme() {
		return compNme;
	}

	public void setCompNme(String compNme) {
		this.compNme = compNme;
	}

	public String getPos() {
		return pos;
	}

	public void setPos(String pos) {
		this.pos = pos;
	}

	public String getPhne() {
		return phne;
	}

	public void setPhne(String phne) {
		this.phne = phne;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	
}
